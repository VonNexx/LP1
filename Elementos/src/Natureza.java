public class Natureza {
    private double biodiversidade;
    private double sustentabilidade;
    private double resiliencia;

    public Natureza(double biodiversidade, double sustentabilidade, double resiliencia) {
        this.biodiversidade = biodiversidade;
        this.sustentabilidade = sustentabilidade;
        this.resiliencia = resiliencia;
    }

    public double getBiodiversidade() {
        return biodiversidade;
    }

    public double getSustentabilidade() {
        return sustentabilidade;
    }

    public double getResiliencia() {
        return resiliencia;
    }
}
