public class Gravidade {
    private double Força;
    private double Peso;
    private double Constante;

    public Gravidade (double Força, double Peso, double Constante) {
        this.Força = Força;
        this.Peso = Peso;
        this.Constante = Constante;
    }

    public double getForça() {
        return Força;
    }

    public double getPeso() {
        return Peso;
    }

    public double getConstante() {
        return Constante;
    }

    
}
